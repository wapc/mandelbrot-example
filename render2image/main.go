package main

import (
	"context"
	"fmt"
	"image"
	"image/color"
	"image/png"
	"io/ioutil"
	"os"

	"github.com/wapc/wapc-go"

	"github.com/wapc/mandelbrot-example/render2image/module"
)

func main() {
	width := 2418
	height := 1208

	if len(os.Args) < 3 {
		fmt.Println("usage: render2image <wasm file> <output file>")
		return
	}
	name := os.Args[1]
	ctx := context.Background()
	code, err := ioutil.ReadFile(name)
	if err != nil {
		panic(err)
	}

	mod, err := wapc.New(code, wapc.NoOpHostCallHandler)
	if err != nil {
		panic(err)
	}
	mod.SetLogger(wapc.Println) // Send __console_log calls to stardard out
	mod.SetWriter(wapc.Print)   // Send WASI fd_write calls to stardard out
	defer mod.Close()

	instance, err := mod.Instantiate()
	if err != nil {
		panic(err)
	}
	defer instance.Close()

	mandelbrot := module.New(instance)
	buffer, err := mandelbrot.Update(ctx, uint32(width), uint32(height), 40)
	if err != nil {
		panic(err)
	}

	img := image.NewRGBA(image.Rectangle{
		Max: image.Point{
			X: width,
			Y: height,
		},
	})

	for y := 0; y < height; y++ {
		yx := y * width
		for x := 0; x < width; x++ {
			c := colors[buffer[yx+x]]
			img.SetRGBA(x, y, color.RGBA{
				R: uint8(c >> 0 & 0xff),
				G: uint8(c >> 8 & 0xff),
				B: uint8(c >> 16 & 0xff),
				A: 255,
			})
		}
	}

	f, err := os.Create(os.Args[2])
	if err != nil {
		panic(err)
	}
	defer f.Close()

	// Encode to `PNG` with `DefaultCompression` level
	// then save to file
	err = png.Encode(f, img)
	if err != nil {
		panic(err)
	}
}

